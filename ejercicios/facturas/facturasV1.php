    // IF - 
    if ($("#carreras").val() == "" || $("#siglas").val() == "" || $("#tetra").val() == "" || $("#fecha_inicial").val() == "" || $("#fecha_final").val() == "" ) {
     alertify.success("Favor de rellenar todos los datos");
